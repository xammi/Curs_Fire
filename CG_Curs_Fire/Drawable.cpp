#include "Drawable.h"

//-------------------------------------------------------------------------------------------------
Drawable::Drawable() :
    lX(0), rX(0), lY(0), rY(0), lZ(0), rZ(0)
{}
//-------------------------------------------------------------------------------------------------
Point3D Drawable::center() const {
    double X = lX + (rX - lX) / 2,
           Y = lY + (rY - lY) / 2,
           Z = lZ + (rZ - lZ) / 2;
    return Point3D(X, Y, Z);
}
